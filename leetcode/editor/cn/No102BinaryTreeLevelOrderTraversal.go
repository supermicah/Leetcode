/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func levelOrder1(root *TreeNode) [][]int {
	if root == nil {
		return nil
	}

	var (
		result [][]int
		stack  []*TreeNode
	)

	stack = append(stack, root)

	for len(stack) > 0 {
		l := len(stack)
		var ans []int
		for i := 0; i < l; i++ {
			node := stack[0]
			stack = stack[1:]
			ans = append(ans, node.Val)
			if node.Left != nil {
				stack = append(stack, node.Left)
			}
			if node.Right != nil {
				stack = append(stack, node.Right)
			}
		}
		if len(ans) > 0 {
			result = append(result, ans)
		}
	}
	return result
}

